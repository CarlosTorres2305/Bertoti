
public class Cedula {
	float valor;
	int quantidade;
	
	
	public Cedula(float valor, int quantidade) {
	
		this.valor = valor;
		this.quantidade = quantidade;
	}
	public float getValor() {
		return valor;
	}
	public void setValor(float valor) {
		this.valor = valor;
	}
	public int getQuantidade() {
		return quantidade;
	}
	public void setQuantidade(int quantidade) {
		this.quantidade = quantidade;
	}
	

	
}
